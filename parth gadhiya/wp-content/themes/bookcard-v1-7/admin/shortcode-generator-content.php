<script>
	jQuery(document).ready(function($)
	{
		var sh_selected = "";
		
		$( '.sh-list' ).change( function()
		{
			$( '.sh-wrap div' ).hide();
			
			sh_selected = $( '.sh-list' ).val();
			
			$( '.' + sh_selected ).show();
		});
		
		
		// insert button
		$( '.btn-sh-insert' ).click( function()
		{
			// sh_out
			var sh_out = "";
			
			if ( sh_selected == 'row' )
			{
				var row_content = $( '#row_content' ).val();
				
				sh_out = '[row]' + row_content + '[/row]';
			}
			else if ( sh_selected == 'column' )
			{
				var column_width = $( '#column_width' ).val();
				var column_offset = $( '#column_offset' ).val();
				var column_content = $( '#column_content' ).val();
				
				sh_out = '[column width="' + column_width + '" offset="' + column_offset + '"]' + column_content + '[/column]';
			}
			else if ( sh_selected == 'divider' )
			{
				sh_out = '[divider]';
			}
			else if ( sh_selected == 'cover_caption' )
			{
				var cover_caption_title = $( '#cover_caption_title' ).val();
				var cover_caption_content = $( '#cover_caption_content' ).val();
				
				sh_out = '[cover_caption title="' + cover_caption_title + '"]' + cover_caption_content + '[/cover_caption]';
			}
			else if ( sh_selected == 'section_caption' )
			{
				var section_caption_title = $( '#section_caption_title' ).val();
				
				sh_out = '[section_caption title="' + section_caption_title + '"]';
			}
			else if ( sh_selected == 'label' )
			{
				var label_content = $( '#label_content' ).val();
				
				sh_out = '[label]' + label_content + '[/label]';
			}
			else if ( sh_selected == 'about_text' )
			{
				var about_text_content = $( '#about_text_content' ).val();
				
				sh_out = '[about_text]' + about_text_content + '[/about_text]';
			}
			else if ( sh_selected == 'portfolio_field' )
			{
				var portfolio_field_content = $( '#portfolio_field_content' ).val();
				
				sh_out = '[portfolio_field]' + portfolio_field_content + '[/portfolio_field]';
			}
			else if ( sh_selected == 'image' )
			{
				var image_src = $( '#image_src' ).val();
				var image_alt = $( '#image_alt' ).val();
				var image_title = $( '#image_title' ).val();
				var image_width = $( '#image_width' ).val();
				var image_height = $( '#image_height' ).val();
				
				sh_out = '[image src="' + image_src + '" alt="' + image_alt + '" title="' + image_title + '" width="' + image_width + '" height="' + image_height + '"]';
			}
			else if ( sh_selected == 'tags' )
			{
				var tags_content = $( '#tags_content' ).val();
				
				sh_out = '[tags]' + tags_content + '[/tags]';
			}
			else if ( sh_selected == 'tag' )
			{
				var tag_text = $( '#tag_text' ).val();
				
				sh_out = '[tag text="' + tag_text + '"]';
			}
			else if ( sh_selected == 'map' )
			{
				var map_content = $( '#map_content' ).val();
				
				sh_out = '[map]' + map_content + '[/map]';
			}
			else if ( sh_selected == 'social_icons' )
			{
				var social_icons_content = $( '#social_icons_content' ).val();
				
				sh_out = '[social_icons]' + social_icons_content + '[/social_icons]';
			}
			else if ( sh_selected == 'social_icon' )
